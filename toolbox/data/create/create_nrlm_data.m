%% create_nrlm_data.m
% Astrodynamics Toolbox
%
% Creates data for the NRLMSISE-00 atmospheric model.
% 
% Author: Tamas Kis
% Last Update: 2022-02-15
%
% ----------------------
% Generated data format.
% ----------------------
%
% This script produces "nrlm_data.mat" which stores the 1×1 struct
% "nrlm_data" with the following fields:
%   • pt    (1×150 double)
%   • pd    (9×150 double)
%   • ps    (1×150 double)
%   • pdl   (2×25 double)
%   • ptm   (1×10 double)
%   • pdm   (8×10 double)
%   • ptl   (4×100 double)
%   • pma   (10×100 double)
%   • sam   (1×100 double)
%   • pavgm (1×10 double)
%
% ----------------
% Raw data format.
% ----------------
%
% The raw data is originally found in "nrlmsise-00_data.c" from the C/C++
% implementation of the NRLMSISE-00 model (written by Dominik Brodowski,
% can be found at https://www.brodo.de/space/nrlmsise/index.html). However,
% this data is already included in "nrl_coeff.m" in a MATLAB File Exchange
% post (adaptation of "nrlmsise-00_data.c" to MATLAB, written by Meysam
% Mahooti, can be found at https://www.mathworks.com/matlabcentral/fileexch
% ange/56253-nrlmsise-00-atmosphere-model?s_tid=srchtitle). The data in
% this script is copied directly from "nrl_coeff.m".



